import Foundation

extension UIColor {
  
  class func ZKJS_themeColor() -> UIColor {
    return UIColor(hexString: "03A9F4")
  }
  
  
  class func ZKJS_textColor() -> UIColor {
    return UIColor.hx_colorWithHexString("666666")
  }
  
  class func ZKJS_navTitleColor() -> UIColor {
    return UIColor.hx_colorWithHexString("5e5e5e")
  }
  
  class func ZKJS_promptColor() -> UIColor {
    return UIColor.hx_colorWithHexString("b8b8b8")
  }
  
  class func ZKJS_lineColor() -> UIColor {
    return UIColor.hx_colorWithHexString("e3e3e3")
  }
  
  class func ZKJS_blackColor() -> UIColor {
    return UIColor.hx_colorWithHexString("101010")
  }
  
  class func ZKJS_whiteColor() -> UIColor {
    return UIColor.hx_colorWithHexString("f8f8f8")
  }
  class func ZKJS_navegationTextColor() -> UIColor {
    return UIColor.hx_colorWithHexString("#949494")
  }
  
}